package com.garow.base.pojo.config;
/**
 * 奖励引用值
 * @author seg
 *
 */
public class RewardData {
	/**
	 * 分类下的id
	 */
	private int id;
	/**
	 * 一级分类
	 */
	private String category;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
}
